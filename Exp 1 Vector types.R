# Create a numeric vector
num_vector <- c(10, 20, 30, 40, 50)

# Create a character vector
char_vector <- c("Apple", "Banana", "Mango", "Orange")

# Create a logical vector
log_vector <- c(TRUE, FALSE, TRUE, FALSE)

# Display contents of vectors
print(num_vector)
print(char_vector)
print(log_vector)

# Display type of each vector
print(typeof(num_vector))
print(typeof(char_vector))
print(typeof(log_vector))